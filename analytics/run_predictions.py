"""Compute and upsert win probability outputs.

This runner walks forward through every match in chronological order. For each
match it reads the Elo ratings as they stood before kickoff, fits the Dixon-Coles
model on the matches that came before, predicts the result, and stores the
prediction. It then updates Elo with the real score and moves on. After the pass
it stores the final team ratings.

Every prediction therefore uses only earlier matches, so the stored numbers are
honest pre-match estimates rather than hindsight. The job is idempotent: it upserts
on the primary key, so re-running produces the same rows and stable row counts. No
probability is generated by a language model.

Run it the same way as the analytics job:

    python -m analytics.run_predictions
    python -m analytics.run_predictions --verify-idempotency
"""

from __future__ import annotations

import argparse
import math
from typing import Any, Iterator

import pandas as pd

from analytics import db
from analytics.dixon_coles import DixonColesConfig, fit_dixon_coles, predict_match
from analytics.elo import EloConfig, EloModel

MODEL_VERSION = "wp-1.0.0"
PREDICTION_TABLES = ("team_ratings", "match_predictions")

MATCHES_QUERY = """
    select
      m.match_id,
      m.match_date,
      m.stage,
      m.home_team_id,
      m.away_team_id,
      ht.name as home_team_name,
      at.name as away_team_name,
      m.home_score,
      m.away_score
    from matches m
    join teams ht on ht.team_id = m.home_team_id
    join teams at on at.team_id = m.away_team_id
    order by m.match_date, m.match_id
"""


def main() -> None:
    parser = argparse.ArgumentParser(description="Run Football AI Pro win probability.")
    parser.add_argument(
        "--verify-idempotency",
        action="store_true",
        help="Run twice and verify prediction table row counts do not change.",
    )
    args = parser.parse_args()

    first_counts = run_once()

    if args.verify_idempotency:
        print("Running win probability a second time to verify idempotency.")
        second_counts = run_once()
        assert_counts_match(first_counts, second_counts)
        print("Idempotency check passed. Row counts were unchanged.")


def run_once() -> dict[str, int]:
    elo_config = EloConfig()
    dixon_coles_config = DixonColesConfig()

    with db.connect() as connection:
        matches = db.read_dataframe(connection, MATCHES_QUERY)

        if matches.empty:
            print("No matches found in the neutral schema.")
            counts = table_counts(connection)
            print_summary(0, 0, counts)
            return counts

        prediction_rows, rating_rows = build_predictions(
            matches,
            elo_config,
            dixon_coles_config,
        )

        db.upsert_rows(connection, "match_predictions", prediction_rows, ("match_id",))
        db.upsert_rows(connection, "team_ratings", rating_rows, ("team_id",))

        counts = table_counts(connection)
        print_summary(len(prediction_rows), len(rating_rows), counts)

        return counts


def iter_walk_forward_predictions(
    matches: pd.DataFrame,
    elo_config: EloConfig,
    dixon_coles_config: DixonColesConfig,
) -> Iterator[tuple[Any, float, float, Any, EloModel]]:
    """Yield a pre-match prediction for every match in chronological order.

    Each match is predicted from the Elo ratings and a Dixon-Coles fit built only
    from earlier matches, then Elo is updated with the real score. The shared
    EloModel is yielded so callers can read the final ratings after the pass. Both
    the runner and the backtest use this so they cannot drift apart.
    """

    elo = EloModel(config=elo_config)

    for position, match in enumerate(matches.itertuples(index=False)):
        home_team_id = str(match.home_team_id)
        away_team_id = str(match.away_team_id)

        prior_matches = matches.iloc[:position]
        home_elo, away_elo = elo.pre_match_ratings(home_team_id, away_team_id)
        fit = fit_dixon_coles(prior_matches, dixon_coles_config)
        prediction = predict_match(
            home_team_id,
            away_team_id,
            home_elo,
            away_elo,
            fit,
            dixon_coles_config,
        )

        yield match, home_elo, away_elo, prediction, elo

        if not pd.isna(match.home_score) and not pd.isna(match.away_score):
            elo.observe(
                home_team_id,
                away_team_id,
                int(match.home_score),
                int(match.away_score),
            )


def build_predictions(
    matches: pd.DataFrame,
    elo_config: EloConfig,
    dixon_coles_config: DixonColesConfig,
) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    prediction_rows: list[dict[str, Any]] = []
    elo: EloModel | None = None

    for match, home_elo, away_elo, prediction, elo in iter_walk_forward_predictions(
        matches,
        elo_config,
        dixon_coles_config,
    ):
        prediction_rows.append(
            to_prediction_row(match, home_elo, away_elo, prediction)
        )

    full_fit = fit_dixon_coles(matches, dixon_coles_config)
    rating_rows = to_rating_rows(elo, full_fit) if elo is not None else []

    return prediction_rows, rating_rows


def to_prediction_row(
    match: Any,
    home_elo: float,
    away_elo: float,
    prediction: Any,
) -> dict[str, Any]:
    return {
        "match_id": str(match.match_id),
        "home_team_id": str(match.home_team_id),
        "away_team_id": str(match.away_team_id),
        "home_team_name": nullable_string(match.home_team_name),
        "away_team_name": nullable_string(match.away_team_name),
        "match_date": match.match_date,
        "stage": nullable_string(match.stage),
        "prob_home_win": round(float(prediction.prob_home_win), 6),
        "prob_draw": round(float(prediction.prob_draw), 6),
        "prob_away_win": round(float(prediction.prob_away_win), 6),
        "expected_home_goals": round(float(prediction.expected_home_goals), 4),
        "expected_away_goals": round(float(prediction.expected_away_goals), 4),
        "most_likely_home_goals": int(prediction.most_likely_home_goals),
        "most_likely_away_goals": int(prediction.most_likely_away_goals),
        "home_elo_pre": round(float(home_elo), 2),
        "away_elo_pre": round(float(away_elo), 2),
        "training_matches": int(prediction.training_matches),
        "model_version": MODEL_VERSION,
    }


def to_rating_rows(elo: EloModel, full_fit: Any) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []

    for team_id, rating in elo.ratings.items():
        attack_strength = None
        defense_strength = None

        if full_fit is not None and team_id in full_fit.attack:
            attack_strength = round(math.exp(full_fit.attack[team_id]), 4)
            defense_strength = round(math.exp(full_fit.defense[team_id]), 4)

        rows.append(
            {
                "team_id": team_id,
                "elo_rating": round(float(rating), 2),
                "attack_strength": attack_strength,
                "defense_strength": defense_strength,
                "matches_played": int(elo.matches_played.get(team_id, 0)),
                "model_version": MODEL_VERSION,
            }
        )

    return rows


def table_counts(connection: Any) -> dict[str, int]:
    return {table: db.count_rows(connection, table) for table in PREDICTION_TABLES}


def assert_counts_match(
    first_counts: dict[str, int],
    second_counts: dict[str, int],
) -> None:
    differences = [
        table
        for table in PREDICTION_TABLES
        if first_counts[table] != second_counts[table]
    ]

    if not differences:
        return

    detail = ", ".join(
        f"{table}: {first_counts[table]} then {second_counts[table]}"
        for table in differences
    )
    raise RuntimeError(f"Idempotency check failed. Row count changes: {detail}")


def print_summary(
    prediction_count: int,
    rating_count: int,
    final_counts: dict[str, int],
) -> None:
    print("Win probability row summary:")
    print(
        f"match_predictions: wrote {prediction_count}, "
        f"final rows {final_counts.get('match_predictions', 0)}"
    )
    print(
        f"team_ratings: wrote {rating_count}, "
        f"final rows {final_counts.get('team_ratings', 0)}"
    )


def nullable_string(value: Any) -> str | None:
    if value is None or pd.isna(value):
        return None

    return str(value)


if __name__ == "__main__":
    main()
